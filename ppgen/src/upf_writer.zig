    const r = [_]f64{ 0.0, 0.01, 0.02 };
    const rab = [_]f64{ 0.01, 0.01, 0.01 };
    const v_local = [_]f64{ -10.0, -9.5, -9.0 };
    const rho_atom = [_]f64{ 0.0, 0.5, 0.3 };
    const dij = [_]f64{};

    const data = PseudopotentialData{
        .element = "He",
        .z = 2,
        .z_valence = 2.0,
        .xc_functional = "PZ",
        .r = &r,
        .rab = &rab,
        .v_local = &v_local,
        .betas = &[_]BetaData{},
        .dij = &dij,
        .rho_atom = &rho_atom,
        .l_max = 0,
        .mesh_size = 3,
    };

    var buf2: [8192]u8 = undefined;
    var writer = std.Io.Writer.fixed(&buf2);

    try write(data, &writer);

