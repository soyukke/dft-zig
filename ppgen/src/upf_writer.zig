test "UPF writer: produces valid XML structure" {
    // Minimal test data
    const r = [_]f64{ 0.0, 0.01, 0.02, 0.03, 0.04 };
    const rab = [_]f64{ 0.01, 0.01, 0.01, 0.01, 0.01 };
    const v_local = [_]f64{ -1.0, -0.99, -0.95, -0.90, -0.85 };
    const beta_vals = [_]f64{ 0.0, 0.1, 0.2, 0.15, 0.05 };
    const dij = [_]f64{1.5};
    const rho_atom = [_]f64{ 0.0, 0.001, 0.002, 0.002, 0.001 };

    const betas = [_]BetaData{
        .{ .l = 0, .values = &beta_vals, .cutoff_index = 5 },
    };

    const data = PseudopotentialData{
        .element = "H",
        .z = 1,
        .z_valence = 1.0,
        .xc_functional = "PZ",
        .r = &r,
        .rab = &rab,
        .v_local = &v_local,
        .betas = &betas,
        .dij = &dij,
        .rho_atom = &rho_atom,
        .l_max = 0,
        .mesh_size = 5,
    };

    var buf: [16384]u8 = undefined;
    var fbs = std.io.fixedBufferStream(&buf);

    try write(data, fbs.writer());

    const output = fbs.getWritten();

    // Check key XML tags are present
    try std.testing.expect(std.mem.indexOf(u8, output, "<UPF version=") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "</UPF>") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "<PP_HEADER") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "<PP_LOCAL") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "<PP_NONLOCAL>") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "<PP_BETA.1") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "<PP_DIJ") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "<PP_RHOATOM") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "element=\"H\"") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "pseudo_type=\"NC\"") != null);
}

test "UPF writer: round-trip with parser (structure)" {
    const r = [_]f64{ 0.0, 0.01, 0.02 };
    const rab = [_]f64{ 0.01, 0.01, 0.01 };
    const v_local = [_]f64{ -10.0, -9.5, -9.0 };
    const rho_atom = [_]f64{ 0.0, 0.5, 0.3 };
    const dij = [_]f64{};

    const data = PseudopotentialData{
        .element = "He",
        .z = 2,
        .z_valence = 2.0,
        .xc_functional = "PZ",
        .r = &r,
        .rab = &rab,
        .v_local = &v_local,
        .betas = &[_]BetaData{},
        .dij = &dij,
        .rho_atom = &rho_atom,
        .l_max = 0,
        .mesh_size = 3,
    };

    var buf2: [8192]u8 = undefined;
    var fbs = std.io.fixedBufferStream(&buf2);

    try write(data, fbs.writer());

    const output = fbs.getWritten();
    try std.testing.expect(std.mem.indexOf(u8, output, "z_valence=\"2.0\"") != null);
    try std.testing.expect(std.mem.indexOf(u8, output, "number_of_proj=\"0\"") != null);
}
